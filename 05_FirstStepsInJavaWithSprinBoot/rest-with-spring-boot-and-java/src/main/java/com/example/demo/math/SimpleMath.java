package com.example.demo.math;

public class SimpleMath{
	/*SOMA________________________________________________________________________________________________________*/
	public Double sun( Double numberOne, Double numberTwo) {
		return numberOne + numberTwo;
	}
	
	/*MÉDIA________________________________________________________________________________________________________*/ 
	public Double mean(Double numberOne,Double numberTwo) {
		return (numberOne) + (numberTwo) / 2;
	}
	
	/*RAIZ QUADRADA________________________________________________________________________________________________*/
	public Double squareRoot(Double number) {
		return Math.sqrt(number);
	}
	
	/*SUBTRAÇÃO____________________________________________________________________________________________________*/
	public Double subtraction(Double numberOne,Double numberTwo) {
		return numberOne - numberTwo;
	}
	
	/*DIVISÃO______________________________________________________________________________________________________*/
	public Double division(Double numberOne,Double numberTwo) {
		return numberOne / numberTwo;
	}
	
	/*MULTIPLICAÇÃO_________________________________________________________________________________________________*/
	public Double multiplication(Double numberOne,Double numberTwo) {
		return numberOne * numberTwo;
	}
}
